function celsiusToFahrenheit(celsius) {
    let celNum = celsius;
    let result = celNum * 9 / 5 + 32;
    return result;

  }

  console.log(celsiusToFahrenheit(0)); // 32
  console.log(celsiusToFahrenheit(25)); // 77

